
def summ(number_1: int, number_2: int) -> int:
    return number_1 + number_2

def subs(number_1: int, number_2: int) -> int:
    return number_1 - number_2

def mult(number_1: int, number_2: int) -> int:
    return number_1 * number_2

def div(number_1: int, number_2: int) -> int:
    return number_1 // number_2